    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO We have to protect against wrong input. Also, maybe complete birthday and stuff.

        String nome, morada, eMail, telefone, bi;

        nome = jTextField2.getText();
        morada = jTextField3.getText();
        bi = jTextField4.getText();
        telefone = jTextField6.getText();
        eMail = jTextField5.getText();
        int [] date = Validation.checkInsertedDate(dateText2Reader.getText(), false);

        if(!nome.equals("") && !morada.equals("") &&  Validation.isInteger(bi)
                && (!telefone.equals("") && Validation.isInteger(telefone) || telefone.equals("")) && date!=null){
            int value = Constants.dbhandler.addPerson(nome, morada, bi, telefone, eMail,date, false);

            if (value == 0){
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
                jTextField6.setText("");
                jTextField5.setText("");
                dateText2Reader.setText("");
                JOptionPane.showMessageDialog(this, "Client added successfully!");
            }
            else if(value == -1)
            {
                JOptionPane.showMessageDialog(this, "There's already a client with such ID Card number!");
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Client couldn't be added to the database...");
            }
        }
        else {
            JOptionPane.showMessageDialog(this, "Invalid Data");
        }

    }//GEN-LAST:event_jButton2ActionPerformed

