    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO We have to protect against wrong input. Also, maybe complete birthday and stuff.

        String nome, morada, eMail, telefone, bi;

        nome = jTextField2.getText();
        morada = jTextField3.getText();
        bi = jTextField4.getText();
        telefone = jTextField6.getText();
        eMail = jTextField5.getText();

        int [] date = Validation.checkInsertedDate(BirthdayTextEmp2.getText(), false);

        if(!nome.equals("") && !morada.equals("") &&  Validation.isInteger(bi)
                        && (!telefone.equals("") && Validation.isInteger(telefone) || telefone.equals("")) && date!=null){
            int value = Constants.dbhandler.addPerson(nome, morada, bi, telefone, eMail, date, true);

            

            if (value == 0){
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
                jTextField6.setText("");
                jTextField5.setText("");
                BirthdayTextEmp2.setText("");
                JOptionPane.showMessageDialog(this, "Employee added successfully!");
            }
            else if(value == -1)
            {
                JOptionPane.showMessageDialog(this, "There's already an employee with such ID Card number!");
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Employee couldn't be added to the database...");
            }
            
        } else {
            JOptionPane.showMessageDialog(this, "Invalid data");
        }



    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String orderBy = (((String) jComboBox1.getSelectedItem()).equals("Name") ? "nome_pessoa" : "id_pessoa");
        ArrayList <Person> list = Constants.dbhandler.getPersonsList(true, orderBy);

        DefaultListModel model = (DefaultListModel) jList1.getModel();
        model.clear();

        for (int i = 0; i < list.size(); i++){
            String info = list.get(i).getNome() + " ID: " + list.get(i).getId();
            model.addElement(info);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

